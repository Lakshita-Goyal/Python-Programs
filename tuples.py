mytuple=[1,3,56,45]
print(mytuple[3])

#append
mytuple1=["ram","shyam"]

mytuple.append(mytuple1)
print(mytuple1)


#slicing
print("tuple[1:2]=",mytuple[1:2])
print("tuple[3:4]=",mytuple[3:4])

#length of tuple
print("length of tuple=",len(mytuple1))

#indexing
print(mytuple)

#repetition
repeated_mytuple=mytuple1*2
print(repeated_mytuple)

#concatenation
mytuple2=mytuple+mytuple1
print(mytuple2)
